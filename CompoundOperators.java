class CompoundOperators{
    public static void main(String args[]){
        int num1 = 101;
        System.out.println("Addition Assignment Operator : " + (num1 += 1));
        System.out.println("Subtraction Assignment Operator : " + (num1 -= 11));
        System.out.println("Multiplication Assignment Operator : " + (num1 *= 111));
        System.out.println("Division Assignment Operator : " + (num1 /= 10));
        System.out.println("Modulus Assignment Operator : " + (num1 %= 101));

    }
}